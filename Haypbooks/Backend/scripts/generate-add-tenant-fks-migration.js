const fs = require('fs')
const path = require('path')
const { Client } = require('pg')

const DEFAULT_DB = 'postgresql://postgres:Ninetails45@localhost:5432/haypbooks_test'
const modelsPath = path.resolve(__dirname, './ci/expected_schema.json')

function timestamp() {
  const d = new Date()
  const YYYY = d.getUTCFullYear()
  const MM = String(d.getUTCMonth() + 1).padStart(2, '0')
  const DD = String(d.getUTCDate()).padStart(2, '0')
  const HH = String(d.getUTCHours()).padStart(2, '0')
  const mm = String(d.getUTCMinutes()).padStart(2, '0')
  const ss = String(d.getUTCSeconds()).padStart(2, '0')
  return `${YYYY}${MM}${DD}${HH}${mm}${ss}`
}

async function main() {
  const expected = JSON.parse(fs.readFileSync(modelsPath, 'utf8'))
  const tenantScoped = expected.filter(m => m.tenantScoped)
  const c = new Client({ connectionString: process.env.DATABASE_URL || DEFAULT_DB })
  await c.connect()

  const toAdd = []
  const toValidate = []
  const toDrop = []
  for (const m of tenantScoped) {
    if (process.env.DEBUG) console.log(`Checking ${m.name}...`)
    const res = await c.query(`
      SELECT tc.constraint_name
      FROM information_schema.table_constraints tc
      JOIN information_schema.key_column_usage kcu ON tc.constraint_name = kcu.constraint_name
      JOIN information_schema.referential_constraints rc ON tc.constraint_name = rc.constraint_name
      WHERE tc.constraint_type = 'FOREIGN KEY'
        AND kcu.column_name = 'tenantId'
        AND (tc.table_name = $1 OR tc.table_name = $2)
        AND rc.unique_constraint_name IN (
          SELECT constraint_name FROM information_schema.table_constraints WHERE table_name = 'Tenant' AND constraint_type IN ('PRIMARY KEY', 'UNIQUE')
        )
    `, [m.name.toLowerCase(), m.name])
    if (process.env.DEBUG) console.log('  found constraints:', res.rows.map(r=>r.constraint_name))
    if (res.rowCount === 0) {
      toAdd.push(m.name)
      continue
    }
    // check all matching constraints for validation status
    for (const r of res.rows) {
      if (process.env.DEBUG) console.log('    checking constraint:', r.constraint_name)
      const chk = await c.query(`
        SELECT pg_c.conname, pg_c.convalidated
        FROM pg_constraint pg_c
        WHERE pg_c.contype = 'f'
          AND pg_c.conname = $1
      `, [r.constraint_name])
      if (process.env.DEBUG) console.log('      chk:', chk.rows)
      if (chk.rowCount > 0 && chk.rows[0].convalidated === false) {
        // if there exists a validated tenantId fk, prefer to drop this unvalidated fk duplicates
        const validated = await c.query(`
          SELECT pg_c.conname
          FROM pg_constraint pg_c
          JOIN pg_class cls ON pg_c.conrelid = cls.oid
          JOIN pg_attribute a ON a.attrelid = cls.oid AND a.attnum = ANY(pg_c.conkey)
          WHERE pg_c.contype = 'f' AND cls.relname = $1 AND a.attname = 'tenantId' AND pg_c.convalidated = true
        `, [m.name.toLowerCase()])
        if (validated.rowCount > 0) {
          toDrop.push({ table: m.name, constraint: chk.rows[0].conname })
        } else {
          toValidate.push({ table: m.name, constraint: chk.rows[0].conname })
        }
      }
    }
  }

  if (process.env.DEBUG) {
    console.log('DEBUG: toAdd=', JSON.stringify(toAdd))
    console.log('DEBUG: toValidate=', JSON.stringify(toValidate))
    console.log('DEBUG: toDrop=', JSON.stringify(toDrop))
  }
  if (toAdd.length === 0 && toValidate.length === 0 && toDrop.length === 0) {
    console.log('No missing tenant FK constraints detected; nothing to generate.')
    await c.end()
    return
  }

  const ts = timestamp()
  const migrationDir = path.resolve(__dirname, `../../prisma/migrations/${ts}_add_missing_tenant_fks`)
  fs.mkdirSync(migrationDir, { recursive: true })
  const migrationFile = path.join(migrationDir, 'migration.sql')

  const addSql = toAdd.map(n => `ALTER TABLE public."${n}" ADD CONSTRAINT "${n}_tenant_fkey" FOREIGN KEY ("tenantId") REFERENCES public."Tenant" ("id") ON DELETE RESTRICT NOT VALID;`).join('\n')
  const validateSql = toValidate.map(t => `ALTER TABLE public."${t.table}" VALIDATE CONSTRAINT "${t.constraint}";`).join('\n')
  const dropSql = toDrop.map(t => `ALTER TABLE public."${t.table}" DROP CONSTRAINT "${t.constraint}";`).join('\n')
  const sqlStatements = [addSql, validateSql, dropSql].filter(Boolean).join('\n')
  fs.writeFileSync(migrationFile, `-- Migration generated by scripts/generate-add-tenant-fks-migration.js\n${sqlStatements}\n`)
  console.log(`Generated migration: ${migrationFile}`)
  console.log(`Wrote file: ${migrationFile}`)

  await c.end()
}

main().catch(e => { console.error('❌ Failed to generate migration:', e.message); process.exit(1) })
