const { execSync } = require('child_process')
const fs = require('fs')
const path = require('path')

const MIGRATIONS_DIR = path.join(__dirname, '../../prisma/migrations')
const SCHEMA_PATH = path.join(__dirname, '../../prisma/schema.prisma')
const TMP_SQUASH_SQL = path.join(__dirname, '../../prisma/squashed.sql')

function safeCheckDatabase() {
  const db = process.env.DATABASE_URL || ''
  if (!db) throw new Error('DATABASE_URL is required')
  if (!db.includes('localhost') && !db.includes('127.0.0.1') && !db.includes('haypbooks_test')) {
    throw new Error('Refusing to run squash on a non-local DATABASE_URL. Set DATABASE_URL to your local test DB to proceed.')
  }
}

(async function main(){
  console.log('Starting migrations squash helper')
  const args = process.argv.slice(2)
  const dryRun = args.includes('--dry-run')
  const archive = args.includes('--archive')

  try {
    safeCheckDatabase()
  } catch (err) {
    console.error(err.message)
    process.exit(1)
  }

  // 1) Backup DB - try to use pg_dump if available
  try {
    const dumpFile = path.join(__dirname, '../../prisma/backups/squash-backup-' + Date.now() + '.sql')
    fs.mkdirSync(path.dirname(dumpFile), { recursive: true })
    try {
      console.log('Attempting to create DB backup to', dumpFile)
      execSync(`pg_dump --dbname="$DATABASE_URL" -f "${dumpFile}"`, { stdio: 'inherit', shell: true, env: process.env })
      console.log('Backup complete:', dumpFile)
    } catch (err) {
      console.warn('pg_dump not available or failed; skipping automation of backup. Please ensure you have a DB backup before proceeding.')
    }
  } catch(e){ console.warn('Backup step failed', e) }

  // 2) Generate squashed SQL from empty db to current prisma schema
  console.log('Generating squashed SQL (prisma migrate diff --from-empty)')
  try {
    execSync(`npx prisma migrate diff --from-empty --to-schema-datamodel ${SCHEMA_PATH} --script > ${TMP_SQUASH_SQL}`, { stdio: 'inherit', shell: true })
    console.log('Squashed SQL generated at', TMP_SQUASH_SQL)
  } catch (err) {
    console.error('prisma migrate diff failed', err)
    process.exit(1)
  }

  // 3) Create squashed migration folder (timestamped)
  const timestamp = new Date().toISOString().replace(/[:\.TZ-]/g, '').slice(0, 14)
  const folderName = `${timestamp}_squashed`
  const newMigrationDir = path.join(MIGRATIONS_DIR, folderName)
  console.log('Creating migration folder:', newMigrationDir)
  if (dryRun) {
    console.log('[dry-run] would create folder and migration.sql at', newMigrationDir)
  } else {
    fs.mkdirSync(newMigrationDir, { recursive: true })
    fs.copyFileSync(TMP_SQUASH_SQL, path.join(newMigrationDir, 'migration.sql'))
    fs.writeFileSync(path.join(newMigrationDir, 'README.md'), '# Squashed migration\n\nThis is an auto-generated squashed baseline migration generated by scripts/migrate/squash-migrations.js. Keep original migrations in `prisma/migrations/archived` or `docs/migration-history/` for reference.')
    console.log('Created squashed migration in folder', newMigrationDir)
  }

  // 4) Mark existing migrations as applied except the squashed one
  const existingMigrations = fs.readdirSync(MIGRATIONS_DIR).filter(d => d !== folderName)
  console.log('Found', existingMigrations.length, 'existing migration folders')
  const archiveDir = path.join(MIGRATIONS_DIR, 'archived')
  for (const m of existingMigrations) {
    const fullPath = path.join(MIGRATIONS_DIR, m)
    if (!fs.existsSync(fullPath) || !fs.statSync(fullPath).isDirectory()) continue
    // Confirm we won't mark the newly created folder
    try {
      console.log('Marking as applied:', m)
      if (!dryRun) execSync(`npx prisma migrate resolve --applied ${m}`, { stdio: 'inherit', shell: true })
      if (archive) {
        console.log('Archiving migration folder:', m)
        if (!dryRun) {
          fs.mkdirSync(archiveDir, { recursive: true })
          const dest = path.join(archiveDir, m)
          fs.renameSync(fullPath, dest)
        } else {
          console.log('[dry-run] would move', fullPath, '->', path.join(archiveDir, m))
        }
      }
    } catch (err) {
      console.warn('Could not mark migration as applied (existing DB state may already show it).', m)
    }
  }

  // 5) Show final note
  console.log('\nSQUASHING COMPLETE - Manual Validation Required')
  console.log('- Verify the generated migration file at prisma/migrations/', folderName)
  console.log('- Keep old migrations for reference and run `npx prisma migrate deploy` to apply the squashed migration on target DBs')
  console.log('- After validation, you may optionally remove or move old migration folders to docs/migration-history/')
  console.log('- If you used --archive, the old migrations are moved to prisma/migrations/archived')
})()
